package in.careerscale.training.swetha;

public class TypeCasting {

	public static void main(String[] args)
	{
byte b;
double d= 330.227;
int i= 10000;

System.out.println("conversion from d to b is");
b= (byte) d;
System.out.println(b);

System.out.println("Conversion of i to b is");
b= (byte) i;
System.out.println(b);
}
}
